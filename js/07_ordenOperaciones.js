// Orden de las Operaciones

let resultado;

//Sigue el orden jerarquico de las operaciones
resultado = 20 + 30 * 2; 
//Para controlar esto podemos agragar parentesis
resultado = (20 + 30) * 2; 

console.log(resultado);

// Incrementos 

let puntaje = 10;

// Incremento en 1 ++
puntaje++; //10
++puntaje //12

puntaje-- //11

// Incremento en num 
puntaje += 10; //Incrementa de 10 en 10

console.log(puntaje);
